# 1. Read & think
nums = [2, 4, 6, 8]
total = 0

for n in nums:
    total += n

print(total)


# Think:

# Why did we start total = 0?
# We start with total = 0 because 0 is the neutral value for addition.

# What happens in each loop iteration?
# In each iteration the loop adds the current element to the accumulator.